package works.hop.calc.svc.service;

public class CalcService {

    public int plus(int left, int right){
        return left + right;
    }

    public int minus(int left, int right){
        return left - right;
    }

    public int times(int left, int right){
        return left * right;
    }

    public float divide(float left, float right){
        return left / right;
    }
}
